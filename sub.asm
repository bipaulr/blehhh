DATA SEGMENT
    MSG1 DB 0AH,0DH,'ENTER FIRST NUMBER: $'
    MSG2 DB 0AH,0DH,'ENTER SECOND NUMBER: $'
    MSG3 DB 0AH,0DH,'DIFFERENCE IS: $'
DATA ENDS

CODE SEGMENT
ASSUME CS:CODE, DS:DATA

START:
    MOV AX,DATA
    MOV DS,AX

    LEA DX,MSG1
    MOV AH,09H
    INT 21H

    MOV AH,01H
    INT 21H
    SUB AL,30H
    MOV BL,AL
    MOV AH,01H
    INT 21H
    SUB AL,30H
    MOV BH,AL

    LEA DX,MSG2
    MOV AH,09H
    INT 21H

    MOV AH,01H
    INT 21H
    SUB AL,30H
    MOV CL,AL
    MOV AH,01H
    INT 21H
    SUB AL,30H
    MOV CH,AL

    MOV AL,BL
    MOV AH,BH
    MOV DL,CL
    MOV DH,CH

    MOV BL,AH
    MOV BH,0
    MOV AL,BL
    MOV AH,0
    MOV BL,10
    MUL BL
    ADD AL,BH
    MOV BX,AX

    MOV CL,DH
    MOV CH,0
    MOV AL,CL
    MOV AH,0
    MOV CL,10
    MUL CL
    ADD AL,DL
    MOV CX,AX

    MOV AX,BX
    SUB AX,CX

    LEA DX,MSG3
    MOV AH,09H
    INT 21H

    MOV BL,10
    DIV BL
    ADD AL,30H
    MOV DL,AL
    MOV AH,02H
    INT 21H
    MOV DL,AH
    ADD DL,30H
    MOV AH,02H
    INT 21H

    MOV AH,4CH
    INT 21H

CODE ENDS
END START
