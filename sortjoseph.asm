DATA SEGMENT
    MSG1 DB 10,13, 'ENTER 5 NUMBERS: $'
    MSG2 DB 10,13, 'SORTED ARRAY (ASCENDING): $'
    ARR  DB 5 DUP(?)
DATA ENDS

CODE SEGMENT
ASSUME CS:CODE, DS:DATA

START:
    MOV AX, DATA
    MOV DS, AX

    LEA DX, MSG1
    MOV AH, 09H
    INT 21H

    LEA SI, ARR
    MOV CL, 05H

READ_NUM:
    MOV AH, 01H
    INT 21H
    SUB AL, 30H
    MOV [SI], AL
    INC SI
    DEC CL
    JNZ READ_NUM

    MOV BL, 05H

OUTER_LOOP:
    DEC BL
    JZ DISPLAY

    LEA SI, ARR
    MOV CL, 04H

COMPARE_LOOP:
    MOV AL, [SI]
    MOV DL, [SI+1]
    CMP AL, DL
    JLE NO_SWAP
    MOV [SI], DL
    MOV [SI+1], AL
NO_SWAP:
    INC SI
    DEC CL
    JNZ COMPARE_LOOP
    JMP OUTER_LOOP

DISPLAY:
    LEA DX, MSG2
    MOV AH, 09H
    INT 21H

    LEA SI, ARR
    MOV CL, 05H

PRINT_LOOP:
    MOV DL, [SI]
    ADD DL, 30H
    MOV AH, 02H
    INT 21H
    INC SI
    DEC CL
    JNZ PRINT_LOOP

EXIT:
    MOV AH, 4CH
    INT 21H

CODE ENDS
END START
