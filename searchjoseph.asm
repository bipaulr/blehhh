DATA SEGMENT
MSG1 DB 10,13,'ENTER 5 NUMBERS: $'
MSG2 DB 10,13,'ENTER NUMBER TO SEARCH: $'
MSG3 DB 10,13,'NUMBER FOUND AT POSITION: $'
MSG4 DB 10,13,'NUMBER NOT FOUND$'

ARR DB 5 DUP(?)
KEY DB ?
POS DB ?
DATA ENDS

CODE SEGMENT
ASSUME CS:CODE, DS:DATA

START:
    MOV AX,DATA
    MOV DS,AX

    LEA DX,MSG1
    MOV AH,09H
    INT 21H

    LEA SI,ARR
    MOV CL,05H
    MOV BL,01H

READ_NUM:
    MOV AH,01H
    INT 21H
    SUB AL,30H
    MOV [SI],AL
    INC SI
    DEC CL
    JNZ READ_NUM

    LEA DX,MSG2
    MOV AH,09H
    INT 21H

    MOV AH,01H
    INT 21H
    SUB AL,30H
    MOV KEY,AL

    LEA SI,ARR
    MOV CL,05H
    MOV BL,01H

SEARCH_NEXT:
    MOV AL,[SI]
    CMP AL,KEY
    JE FOUND
    INC SI
    INC BL
    DEC CL
    JNZ SEARCH_NEXT
    JMP NOTFOUND

FOUND:
    LEA DX,MSG3
    MOV AH,09H
    INT 21H

    MOV AL,BL
    ADD AL,30H
    MOV DL,AL
    MOV AH,02H
    INT 21H
    JMP EXIT

NOTFOUND:
    LEA DX,MSG4
    MOV AH,09H
    INT 21H

EXIT:
    MOV AH,4CH
    INT 21H

CODE ENDS
END START
